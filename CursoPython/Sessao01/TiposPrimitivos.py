#No python todos os tipos primitivos são objetos ou seja existem metodos para eles
#Exemplos 

str = "Larissa" #str - string
int = 1121      #int - Inteiro
float = 23.3    #float - Float 
bool = True     #bool = Booleano/logico True ou False


#Type é um metodo que retorna o tipo de dados colocados como parametro
print(type(int))#Int - são numeros inteiros
print(type(str))#str - são textos que estão dentro de aspas simples ou duplas 
print(type(bool))# bool - sã0 opererdores que retornam True ou False 
print(type(float))# float - são numeros com . ex: 2.3 


